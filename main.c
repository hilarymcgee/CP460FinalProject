void printMatrix(unsigned short * state) {
    int i;
    for (i = 0; i < 16; i++) {
        printf("%02x ", state[i]);
        if (i % 4 == 3) {
            printf("\n");
        }
    }
}

void printBinary(unsigned short n, int size) {
    int i;
    for (i = size-1; i >= 0; i--) {
        printf("%d", (n >> i) & 1);
    }
    printf("\n");
}

